package emailApp;
/*
The Department class defines the different departments available in the company.
 Each department is represented as a constant string.
These constants are used to assign a department to an employee and generate their email address based on the department.
 */
public class Department {

    //Constat represtenst the sales, development, accounting

    public static final String Sales = "sales";
    public static final String Development = "development";
    public static final String Accounting = "accounting";
    public static final String NONE = "";  // Constant representing no department (used when an employee has no department)

}
